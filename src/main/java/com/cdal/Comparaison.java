package main.java.com.cdal;

/**
 * Enumération représentant les différents types de comparaisons possibles pour les athlètes.
 */
public enum Comparaison {
    
    /**
     * Comparaison basée sur la force des athlètes.
     */
    FORCE,
    
    /**
     * Comparaison basée sur l'endurance des athlètes.
     */
    ENDURANCE,
    
    /**
     * Comparaison basée sur l'agilité des athlètes.
     */
    AGILITE,
    
    /**
     * Comparaison basée sur la moyenne des différentes capacités des athlètes.
     */
    MOYENNE
}